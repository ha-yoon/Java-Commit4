package condition;

public class Ex01 {

	public static void main(String[] args) {
		
		int x = 2;
		
		switch(x) {
		
		case 1 :
			System.out.println("숫자 1 입니다.");
			break;
		case 2 :
			System.out.println("숫자 2 입니다.");
			break;
		case 3 :
			System.out.println("숫자 3 입니다.");
			break;
		case 4 :
			System.out.println("숫자 4 입니다.");
			break;
			default : 
				System.out.println("숫자 1 ~ 4가 아닙니다.");
		}

	}

}
