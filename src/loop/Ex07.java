package loop;

public class Ex07 {

	public static void main(String[] args) {
				
		int dan;
		int num;
		
		for(num = 1; num < 10; num++) {
			
			for(dan = 2; dan < 10; dan++) {
				System.out.printf("%dx%d=%d\t", dan, num, dan*num);
	        }
			System.out.println();
        }
	
	}
	
	}
		
	 
  
